package prm392.fpt.edu.vn.domain;

public class Category {
    String type;
    String img_url;

    public Category() {
    }

    public String getImg_url() {
        return img_url;
    }

    public void setImg_url(String img_url) {
        this.img_url = img_url;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
